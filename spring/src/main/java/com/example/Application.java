@EnableWebSecurity
class WebSecurityConfig extends WebSecurityConfigurerAdapter {
	@Autowired
	private UserDetailsService myUserDetailsService;
	@Autowired
	private JwtRequestFilter jwtRequestFilter;

	@Autowired
	public void configureGlobal(AuthenticationManagerBuilder auth) throws Exception {
		auth.userDetailsService(myUserDetailsService).passwordEncoder(passwordEncoder());
	}

	@Bean
	public PasswordEncoder passwordEncoder() {
		return new BCryptPasswordEncoder();
	}
	@Override
	@Bean
	public AuthenticationManager authenticationManagerBean() throws Exception {
		return super.authenticationManagerBean();
	}

	@Override
	protected void configure(HttpSecurity httpSecurity) throws Exception {
		httpSecurity.csrf().disable()
				.authorizeRequests().antMatchers("/user/login","/user/signup","/user/login","/admin/signup","/authenticate").permitAll().
				antMatchers("/user/dashboard","/user/bikes","/user/bookBike","/user/editProfile","/user/bikeDetails","/user/bookings").hasRole("USER").
				antMatchers("/admin/Profile","/admin/editProfile","/admin/addBike","/admin/editBike","/admin/deleteBike","/admin/dashboard").hasRole("ADMIN").
						anyRequest().authenticated().and().
						exceptionHandling().and().sessionManagement()
				.sessionCreationPolicy(SessionCreationPolicy.STATELESS);
		httpSecurity.addFilterBefore(jwtRequestFilter, UsernamePasswordAuthenticationFilter.class);
		httpSecurity.headers()
        .addHeaderWriter(
                new StaticHeadersWriter("Access-Control-Allow-Origin", "http://localhost:4200")
        );
	}

	public WebSecurityConfig() {
	}

}
